public class DoubleNumberComputation {

	public static void main(String[] args) {
		double d1 = 5.1;
		double d2 = 0.1;
		double sum = d1 + d2;
		if (sum == 5.2) {
			System.out.println(sum);
		} else {
			System.out.println(sum);
			System.out.println("Output is not 5.2 as expected as java do some conversion before computation");
		}
	}

}
